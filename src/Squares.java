/**
This program demonstrates the for loop.
*/

public class Squares
{
   public static void main(String[] args)
   {
      int number=1;
       // Loop control variable
      
      System.out.println("Number Number Squared");
      System.out.println("------------------------");
      do
      {
      ++number;
      System.out.println(number + "\t\t" + number * number);
      }
      while (number<10);
         }
            
            }
